function collide (foods: any[], enemies: any[]) {
    for (let value of foods) {
        if (plyr1.overlapsWith(value) == true) {
            value.destroy()
            game.splash("Capture the enemy quickly, you only have 3 seconds!")
            info.startCountdown(4)
            ateFood = true
        }
    }
    for (let value of enemies) {
        if (plyr1.overlapsWith(value) == true && ateFood == true) {
            value.destroy()
            info.stopCountdown()
            ateFood = false
        }
    }
}
